n1 = int(input('Digite algum número: '))
n2 = int(input('Digite outro número: '))
s = n1 + n2
su = n1 - n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
rd = n1 % n2
p = n1 ** n2
print(' a soma vale: {} \n a subtração vale: {} \n a multiplicação vale: {} \n a divisão vale: {:.3f} \n a divisão inteira vale: {} \n o resto da divisão é: {} \n a potenciação é: {} \n '.format(s, su, m, d, di, rd, p))